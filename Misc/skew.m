%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% y [3x3]: Skew symmetric matrix for the given vector x

function y = skew(x)

x1 = x(1); x2 = x(2); x3 = x(3);

y = [0 -x3 x2; 
     x3 0 -x1; 
    -x2 x1 0];